import re
def segmentar_secciones(texto):
    """
    Segmenta el texto en secciones numeradas 3, 4 y 5
    Según títulos que comienzan con "3.", "4." o "5."
    Retorna diccionario con secciones como claves y texto como valor.
    """
    # Buscar los encabezados de seccion 3,4,5 usando regex
    patrones = {
        '3': r'(?:^|\n)(3\..*?)(?=\n[4]\.|[5]\.|$)',  # Sección 3 hasta antes de 4 o 5 o fin
        '4': r'(?:^|\n)(4\..*?)(?=\n[5]\.|$)',        # Sección 4 hasta antes de 5 o fin
        '5': r'(?:^|\n)(5\..*)'                        # Sección 5 hasta fin
    }
    secciones = {}
    for key, patron in patrones.items():
        match = re.search(patron, texto, re.DOTALL)
        if match:
            secciones[key] = match.group(1).strip()
        else:
            secciones[key] = "Sección no encontrada o vacía."
    return secciones
