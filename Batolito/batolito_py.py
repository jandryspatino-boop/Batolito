@app.post("/generar_mapa_mental")
def generar_mapa_mental(request: ConsultaRequest):
    doc = nlp(request.texto)

    nodos = {}
    relaciones = []

    # Nodo raíz tema completo
    nodos["0"] = {
        "id": "0",
        "data": {"label": request.texto[:60] + "... (Tema)"},
        "position": {"x": 300, "y": 0}
    }

    id_counter = 1

    # Detectar términos geológicos
    matches = matcher(doc)
    nodos_text_lower = set()

    for match_id, start, end in matches:
        span = doc[start:end]
        texto = span.text.lower()
        if texto in nodos_text_lower:
            continue
        nodos_text_lower.add(texto)

        # Categoría para posicionamiento
        categoria = "concepto"
        for cat, lista in categorias.items():
            if texto in lista:
                categoria = cat
                break

        # Posición X según categoría
        if categoria == "proceso":
            pos_x = 100
        elif categoria == "roca":
            pos_x = 400
        else:
            pos_x = 700

        pos_y = 100 + (id_counter * 60)

        nodos[str(id_counter)] = {
            "id": str(id_counter),
            "data": {
                "label": span.text,
                "description": definiciones.get(texto, "Definición no disponible."),
                "categoria": categoria
            },
            "position": {"x": pos_x, "y": pos_y},
        }

        relaciones.append({"id": f"e0-{id_counter}", "source": "0", "target": str(id_counter)})

        id_counter += 1

    # Extraer verbos como procesos adicionales y conectarlos según proximidad
    for sent in doc.sents:
        sent_doc = nlp(sent.text)
        # Términos en esta oración
        terminos_en_sent = [span.text.lower() for match_id, start, end in matcher(sent_doc) for span in [sent_doc[start:end]]]
        verbos = [token.lemma_ for token in sent_doc if token.pos_ == "VERB"]

        for termino in terminos_en_sent:
            term_id = next((k for k, v in nodos.items() if v["data"]["label"].lower() == termino), None)
            if term_id:
                for verbo in verbos:
                    verbo_lower = verbo.lower()
                    # Solo agregar verbo si no es repetido y relevante
                    if verbo_lower in terminos_petro:
                        nodo_verbo_id = None
                        for k, v in nodos.items():
                            if v["data"]["label"].lower() == verbo_lower:
                                nodo_verbo_id = k
                                break
                        if not nodo_verbo_id:
                            nodo_verbo_id = str(id_counter)
                            nodos[nodo_verbo_id] = {
                                "id": nodo_verbo_id,
                                "data": {
                                    "label": verbo.capitalize(),
                                    "description": "Proceso relacionado.",
                                    "categoria": "proceso"
                                },
                                "position": {
                                    "x": nodos[term_id]["position"]["x"] + 150,
                                    "y": nodos[term_id]["position"]["y"] + 20
                                }
                            }
                            id_counter += 1
                        relaciones.append({"id": f"e{term_id}-{nodo_verbo_id}", "source": term_id, "target": nodo_verbo_id})

    elementos = list(nodos.values()) + relaciones
    return {"elements": elementos}

